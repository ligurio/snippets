//------------------------------------------------------------
bool RODOS3_t::STATE__ON() {
    //  включение
    return USB_SET_PORT(0x00);
}
//------------------------------------------------------------
bool RODOS3_t::STATE__OFF() {
    //  выключение
    return USB_SET_PORT(0x19);
}
//------------------------------------------------------------
bool RODOS3_t::STATE__READ(unsigned char &PS) {
    //  чтение состояния
    return USB_GET_PORT(PS);
}
